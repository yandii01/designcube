            'email' => 'required|min:10|email',
        ],[
            'email.required' => 'Email tidak boleh kosong !',
            'email.email' => 'Email tidak valid !',
            'email.min' => 'Email minimal 10 karakter !',
        ]);


        $subscription = Subscription::create([
            'email' => $request->email,
            'ip' => $ip,
        ]);

        return redirect('/')->with('status', 'Sukses!');
    
